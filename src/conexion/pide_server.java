    private void boton_guardar_serverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardar_serverActionPerformed
        
        if (dir_server.getText().isEmpty()) {
                lbl_error_dir_server.setVisible(true);
        } else {
            try{
                            pide_server.this.setEnabled(false);
                     
                            //creo un archivo en el directorio raiz de mi proyecto
                            File archivo = new File("conexion BD_system.txt");
                            //  a.createNewFile();
                            if (archivo.exists()) {
                                BufferedWriter bw;
                                bw = new BufferedWriter(new FileWriter(archivo.getAbsolutePath()));
                                bw.write(dir_server.getText());
                                bw.close();
                            } else {
                                BufferedWriter bw;
                                bw = new BufferedWriter(new FileWriter(archivo.getAbsolutePath()));
                                bw.write("localhost");
                                bw.close();
                            }
                            
                            pide_server.this.setEnabled(true);
                        }catch(Exception e){
                            Logger.getLogger(opciones_de_gestionar_contrato.class.getName()).log(Level.SEVERE, null, e);
                            opciones_de_gestionar_contrato.lanza_error(e);
                        }
        }
             this.dispose();
    }//GEN-LAST:event_boton_guardar_serverActionPerformed

