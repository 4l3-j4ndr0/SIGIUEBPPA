    private double dame_consumo(String chapa){
        double consumo=0;
        String sql_indice_consumo="SELECT `indice_consumo_ekipo` FROM `ekipos` WHERE `chapa_ekipo`='"+chapa+"'";
        consumo=opciones_de_gestionar_hoja_ruta.extraer_indice_consumo(sql_indice_consumo);
        return consumo;
    }
    
    public void accion_guardar(){
        try{
        String chapa = combo_chapa_hoja_ruta.getSelectedItem().toString();
        String km = km_hoja_ruta.getText();
        String consumo = String.valueOf(dame_consumo(chapa)*Double.parseDouble(km));
        File ruta = new File(ruta_archivo);
        if (chapa.trim().length() != 0 && ruta_archivo.trim().length() != 0) {
            guardar_pdf(chapa, km, consumo, ruta);
         //   tpdf.visualizar_PdfVO(tabla);
            ruta_archivo = "";
            reset_campos();
            SuccessAlert e = new SuccessAlert(new JFrame(), true);
            e.msj1.setText("¡Los datos de la hoja de ruta fueron");
            e.msj2.setText("guardados exitosamente!.");
            e.msj3.setText("");
            e.setVisible(true);
        } 
        }catch(Exception ex){
            opciones_de_gestionar_contrato.lanza_error(ex);
        }
    }
    
    public void guardar_pdf( String chapa, String km, String consumo, File ruta) {
        PdfDAO_hoja_ruta pa = new PdfDAO_hoja_ruta();
        PdfVO_hoja_ruta po = new PdfVO_hoja_ruta();
        po.setChapa(chapa);
        po.setTotal_km(km);
        po.setConsumo(consumo);
        try {
            byte[] pdf = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(pdf);
            po.setArchivopdf(pdf);
        } catch (IOException ex) {
            po.setArchivopdf(null);
        }
        pa.Agregar_PdfVO(po);
    }
    
